package in.co.rays.basics;

public class SwitchCase {

	public static void main(String[] args) {
		
		int n=3;
		
		switch (n) {
		case 1:
			
			
			System.out.println("you choice one");
			
			
			break;
			
		case 2:
			
			System.out.println("you choice two");
			
			break;

		default:
			
			System.out.println("inavlid option");
			break;
		}
		
		
		
	}
	
	

}
