package in.co.rays.basics;

public class Switch1 {
	public static void main(String[] args) {
		int date = 3;
		switch (date) {
		case 1:
			System.out.println("this is first date of a month");
			break;
		case 2:
			System.out.println("this is second date of a month");
			break;
		default:
			System.out.println("this is invalid date");
			break;
		}
				
	}
}
