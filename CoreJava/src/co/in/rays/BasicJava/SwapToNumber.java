package co.in.rays.BasicJava;
public class SwapToNumber{
public static void main(String[] args){

int  a,b,temp;
	a=44;
	b=26;
System.out.println("Before swaping : ");
System.out.print("a = ");
System.out.println(a);
System.out.print("b = ");
System.out.println(b);
	temp = a;
	a = b;
	b = temp;
System.out.println("After swaping : ");
System.out.print("a = ");
System.out.println(a);
System.out.print("b = ");
System.out.println(b);
}
}